export function SectionInfo({ section }: { section: string }) {
  const [open, setOpen] = useState(false)
  const c = CONTENT[section]
  if (!c) return null
  return (
    <>
      <button onClick={() => setOpen(true)} title="Как считаются показатели"
        className="inline-flex items-center gap-1.5 rounded-lg border border-border bg-card px-3 h-9 text-xs font-semibold text-muted-foreground hover:text-primary hover:border-primary/50 transition-all">
        <Info className="w-4 h-4" /> Как считается
      </button>
      <Modal open={open} onClose={() => setOpen(false)} title={c.title}
        subtitle="Понятное объяснение показателей и формул — без технических деталей" wide>
        <div className="space-y-4">
          {c.blocks.map((b, i) => (
            <div key={i} className="rounded-xl border border-border bg-card p-3">
              <h4 className="text-sm font-black text-foreground mb-1.5">{b.h}</h4>
              <ul className="space-y-1.5">
                {b.lines.map((l, j) => (
                  <li key={j} className="flex gap-2 text-xs text-muted-foreground leading-relaxed">
                    <span className="text-primary shrink-0">•</span><span>{bold(l)}</span>
                  </li>
                ))}
              </ul>
            </div>
          ))}
        </div>
      </Modal>
    </>
  )
}
